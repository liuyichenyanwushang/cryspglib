def generate_static_data(content):
    code = []
    
    # 3x3 Int Matrices
    int_matrices = ['M_bcc', 'M_fcc', 'M_ac', 'M_bc', 'M_cc', 'M_rc']
    for name in int_matrices:
        data = extract_array_content(content, name)
        if data:
            code.append(f"static {name.upper()}: [[i32; 3]; 3] = {data};")

    # 3x3 Double Matrices
    dbl_matrices = ['M_bcc_inv', 'M_fcc_inv', 'M_ac_inv', 'M_bc_inv', 'M_cc_inv', 'M_rc_inv']
    for name in dbl_matrices:
        data = extract_array_content(content, name)
        if data:
            code.append(f"static {name.upper()}: [[f64; 3]; 3] = {data};")
            
    code.append("")

    # Large Arrays
    # VSpU arrays are double [n][3][9]
    # Generators are int [n][3][9]
    
    arrays = [
        ('tricli_VSpU', 'f64'), ('tricli_generators', 'i32'),
        ('monocli_VSpU', 'f64'), ('monocli_generators', 'i32'),
        ('monocli_A_VSpU', 'f64'), ('monocli_B_VSpU', 'f64'), ('monocli_C_VSpU', 'f64'), ('monocli_I_VSpU', 'f64'),
        ('ortho_VSpU', 'f64'), ('ortho_generators', 'i32'),
        ('ortho_F_VSpU', 'f64'), ('ortho_I_VSpU', 'f64'), ('ortho_A_VSpU', 'f64'), ('ortho_B_VSpU', 'f64'), ('ortho_C_VSpU', 'f64'),
        ('tetra_VSpU', 'f64'), ('tetra_generators', 'i32'), ('tetra_I_VSpU', 'f64'),
        ('trigo_VSpU', 'f64'), ('trigo_generators', 'i32'),
        ('rhombo_h_VSpU', 'f64'), ('rhombo_h_generators', 'i32'),
        ('rhombo_p_VSpU', 'f64'), ('rhombo_p_generators', 'i32'),
        ('hexa_VSpU', 'f64'), ('hexa_generators', 'i32'),
        ('cubic_VSpU', 'f64'), ('cubic_generators', 'i32'),
        ('cubic_F_VSpU', 'f64'), ('cubic_I_VSpU', 'f64'),
    ]
    
    for name, type_name in arrays:
        data = extract_array_content(content, name)
        if data:
            # Count dimensions to determine size
            # This is a bit hacky but works for this specific file structure
            # Count number of opening brackets at top level
            num_entries = data.count('{\n') # C formatting usually puts newline
            if num_entries == 0:
                 num_entries = data.count('{{') # Compact formatting
            
            # Fallback: count commas? No, unreliable.
            # Let's just count the blocks.
            blocks = re.findall(r'\{\s*\{', data)
            size = len(blocks)
            
            rust_name = name.upper()
            code.append(f"static {rust_name}: [[[ {type_name}; 9]; 3]; {size}] = {data};")

    return "\n".join(code)

