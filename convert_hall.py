import re

def parse_c_file(filename):
    with open(filename, 'r') as f:
        content = f.read()
    return content

def extract_array_content(content, array_name, type_hint="int"):
    # 匹配 static type name[...] = { ... };
    # 使用非贪婪匹配和 DOTALL
    pattern = re.compile(r'static\s+\w+\s+' + array_name + r'\[.*?\]\s*=\s*\{(.*?)\};', re.DOTALL)
    match = pattern.search(content)
    if not match:
        return None
    
    body = match.group(1)
    # 移除注释
    body = re.sub(r'/\*.*?\*/', '', body)
    
    # 简单的解析逻辑：将大括号内的内容提取出来
    # 假设是 3D 数组 [n][3][9] 或 2D 数组 [3][3]
    
    # 移除外层大括号（如果有）
    body = body.strip()
    
    # 将 C 的数组语法转换为 Rust
    # {1, 2, 3} -> [1, 2, 3]
    rust_body = body.replace('{', '[').replace('}', ']')
    
    # 处理浮点数后缀 (如 1.0/2.0)
    # Rust 中 1.0/2.0 是合法的，但我们需要确保类型正确
    if type_hint == "f64":
        # 确保整数除法变成浮点
        # 这里简单处理，C代码中已经是 1.0/2.0 这种形式了，Rust 兼容
        pass
        
    return rust_body

